                                                   style="text-align: justify;"><b>Prof. N R Shetty </b>is considered to be one of the top academicians in the country and has been a beacon of light in academic circles. Prof. N.R. Shetty had a brilliant academic career and obtained his B.Sc. (Honours) in Physics with Electronics as a special subject from Madras University, M.Sc. (Opto-Electronics) from University of Essex (U.K.) and Ph.D. from Dept. of Electrical Engineering of IIT Bombay. He has over thirty-five years of teaching experience at Post Graduate Level and Research Experience in the areas of Microwave Engineering, Quantum and Opto Electronics, Solid State Physical Electronics, etc.
