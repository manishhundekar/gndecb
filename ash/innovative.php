          <strong><span style="color: #993366;">Innovative Teaching</span></strong>

<p>The department consists of dedicated, qualified and experienced faculty and supporting staff in various fields of Engineering Chemistry. All the staff members meet the requirements as prescribed by AICTE and VTU University. Various pedagogical activities have been followed by the faculty for the student in order to create interest over the subjects and the developing technologies.</p>
<p>The following are the various teaching methodologies used in different aspects for the delivery of course:</p>
<p><strong>i) Lecture Method and Interactive Learning:</strong></p>

<p>Audiovisual aids, chalk, and board are used by the faculty for teaching.<br>
Students are encouraged to clarify their doubts, during the lecture hour itself.<br>
During the lecture hour first 5 minutes are used for revising topics taught in the previous class and the last 5 minutes is used to give a summary of the current class. Hence the students will get continuity of the previous day topics.<br>

<p><strong>iii) Practical Learning:</strong></p>
<p>Practical exercises are taught in the laboratory.<br>
Virtual Lab exercises are given to the students.<br>
In addition to the experiments given in the syllabus, students are instructed to practice additional experiments as suggested by the concerned faculty.</p>

<p>Laboratories are accessible for the students during the scheduled periods and also during leisure hours for doing experiments.<br>
Charts are displayed in laboratories through which the students can gain basic ideas<br>
<p><strong>iv) Hi-tech classroom:</strong></p>
<p>All classrooms are equipped with projector facility.<br>
All laboratories and classrooms have Wi-fi access.</p>

