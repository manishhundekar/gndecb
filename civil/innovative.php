               <div class="alert alert-primary" role="alert"> <b>Innovative Teaching</b> </div>
               <strong><span style="color: #993366;">Innovative Teaching</span></strong>
               <p>
                  The department consists of dedicated, qualified and experienced faculty and
                  supporting staff in various fields of Civil Engineering. All the staff
                  members meet the requirements as prescribed by AICTE and VTU University.
                  Various pedagogical activities have been followed by the faculty for the
                  student in order to create interest over the subjects and the developing
                  technologies.
               </p>
               <p>
                  The following are the various teaching methodologies used in different
                  aspects for the delivery of course:
               </p>
               <p>
                  <strong>i) Lecture Method and Interactive Learning:</strong>
               </p>
               <p>
                  ● Audiovisual aids, chalk, and board are used by the faculty for teaching.
               </p>
               <p>
                  ● Students are encouraged to clarify their doubts, during the lecture hour
                  itself.
               </p>
               <p>
                  ● During the lecture hour first 5 minutes are used for revising topics
                  taught in the previous class and the last 5 minutes is used to give a
                  summary of the current class. Hence the students will get continuity of the
                  previous day topics.
               </p>
               <p>
                  <strong>ii) Application Based Learning:</strong>
               </p>
               <p>
                  ● Students are encouraged to attend workshops from 2<sup>nd</sup> year
                  onwards. As an outcome of the workshop, interested students will develop
                  Projects/ mini Projects at the end of the workshop.
               </p>
               <p>
                  ● All third-year students are encouraged to do mini projects and are asked
                  to submit the same before the commencement of exams.
               </p>
               <p>
                  ● Students are encouraged for an internship in the industry during
                  vacations.
               </p>
               <p>
                  ● Students are taken to industrial visit at least once a semester to see
                  the real-time operation.
               </p>
               <p>
                  <strong>iii) Practical Learning:</strong>
               </p>
               <p>
                  ● Practical exercises are taught in the laboratory.
               </p>
               <p>
                  ● Virtual Lab exercises are given to the students.
               </p>
               <p>
                  ● In addition to the experiments given in the syllabus, students are
                  instructed to practice additional experiments as suggested by the concerned
                  faculty.
               </p>
               <p>
                  ● Civil software like Autocad-2D&amp; 3D, STAAD. REVIT Architecture, TEKLA
                  (student version) and the laboratory work is broadly based to develop an
                  understanding of the properties of materials, in terms of their structure,
                  use in the construction industry and methods of performance testing. The
                  student to investigate the physical and chemical properties in relation to
                  appreciating why different materials are specified for particular elements
                  within the structure, and why buildings stand up.
               </p>
               <p>
                  ● Laboratories are accessible for the students during the scheduled periods
                  and also during leisure hours for doing experiments.
               </p>
               <p>
                  ● Charts are displayed in laboratories through which the students can gain
                  basic ideas about different equipment such as Universal Testing machine,
                  Theodolite, Elongation and Flakiness index and various Software packages.
               </p>
               <p>
                  ● Students are taken to the nearby construction site, once in their
                  curriculum.
               </p>
               <p>
                  <strong>iv) Hi-tech classroom:</strong>
               </p>
               <p>
                  ● All classrooms are equipped with projector facility.
               </p>
               <p>
                  ● All laboratories and classrooms have Internet access.
               </p>
               <p>
                  <strong>
                  v) Other Innovative Teaching methods adopted by the faculty
                  </strong>
               </p>
               <p>
                  ● <em>Real-Time Demonstration</em> – Models are displayed in laboratories
                  through which the students can see the models of Retaining wall, Suspension
                  cable bridges, Multi-storied buildings,etc.,
               </p>
               <p>
                  ● <em>KSCST Projects </em>– Students are encouraged and guided by faculty
                  members to do real-time projects and funded by KSCST.
               </p>
               <p>
                  ● <em>Online courses</em> – Online courses are being conducted through
                  NPTEL online courses so that students can post and clear their doubts at
                  any time without hesitation and can download course materials.
               </p>
               <p>
                  ● <em>College Website</em> – Each student can download Course Material,
                  Syllabus, Question bank, Assignment topics, Short videos, NPTEL video
                  links.
               </p>
