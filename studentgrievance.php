                           <h5 style="background: #2F92D8;color:#fff;margin-top: 0px;margin-bottom: 10px;padding: 7px;"><i class="fas fa-chalkboard-teacher"></i></i></i>&nbsp Aim and Objectives of Student Grievance Redressal Cell</h5>
                           The Student Grievance Redressal Cell was constituted to address the student grievances.This cell aims to provide Students with a platform where they can submit their grievances for Redressal. This cell ensures the redressal of grievances.<br>
                           <b> Objectives:</b>
                           <br> <b>1.</b>To create a platform where students can raise their grievances, regarding academic and non academic matters.
                           <br> <b>2.</b>To ensure a fair, impartial and consistent way for redressal of various issues faced by the stakeholders.
                           <br> <b>3.</b>To uphold the sanctity of the college by promoting good relationship amongst students, staff and good professional relationship between students, staff and administration.
                           <br> <b>4.</b>To ensure that grievances are addressed and resolved promptly and in complete confidentiality.
                           <br> <b>5.</b> To ensure that the views of grievant and respondent are respected and that any party to a grievance is not  discriminated or victimized.
                           <br> <b>6.</b> To ensure stakeholders to respect the rights and dignity of one another.
                           <br> <b>Grievance" means and includes, complaint(s) made by an aggrieved student in respect of the following namely</b>
                           <br>1.Admission contrary to merit determined in accordance with the declared admission policy of the institution
                           <br>2.Irregularity in the process under the declared admission policy of the institution
                           <br>3.Refusal to admit in accordance with the declared admission policy of the institution
                           <br>4.Non-publication of prospectus by the institution, in accordance with the provisions of these regulations
                           <br>5.Publication by the institution of any information in the prospectus, which is false or misleading, and not based on facts
                           <br>6.Withholding of, or refusal to return, any document in the form of certificates of degree, diploma or any other award or other document deposited by a student for the purpose of seeking admission in such institution, with a view to induce or compel such student to pay any fee or fees in respect of any course or program of study which such student does not intend to pursue
                           <br>7.Demand of money in excess of that specified to be charged in the declared admission policy of the institution
                           <br>8.Violation, by the institution, of any law for the time being in force in regard to reservation of seats in admission to different category of students
                           <br>9.Non-payment or delay in payment of scholarships or financial aid admissible to any student under the declared admission policy of such institution, or under the conditions, if any, prescribed by the Council
                           <br>10.Delay by the institution in the conduct of examinations, or declaration of results, beyond the schedule specified in the academic calendar of the institution, or in such calendar prescribed by the Council
                           <br>11.Failure by the institution to provide student amenities as set out in the prospects, or is required to be extended by the institution under any provisions of law for the time being in force
                           <br>12.Non-transparent or unfair practices adopted by the institution for the evaluation of students
                           <br>13.Delay in, or denial of, the refund of fees due to a student who withdraws admission within the time mentioned in the prospects, or as may be notified by the Council
                           <br>14.Complaints of alleged discrimination of students from Scheduled Castes, Scheduled Tribes, Other Backward Classes, Women, Minority or persons with disabilities categories
                           <br>15.Denial of quality education as promised at the time of admission or required to be provided.
                           <br>16.Harassment or victimization of a students, other than cases of harassment, which are to be proceeded against under the penal provisions of any law for the time being in force
                          
                           <br>17.Complaints of alleged discrimination of student from Scheduled Castes, Scheduled Tribes, Other Backward Classes, Women, Minority or persons with disabilities categories
