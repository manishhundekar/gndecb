                                                   style="text-align: justify;"><b>Shri Samartha</b> is a techno-visionary and accomplished entrepreneur with over two decades of experience in the global telecom industry. He has led large organizations exceeding over USD 100m in revenues and managed teams of over a thousand engineers spanning five countries.<br>
Samartha is currently the strategic advisor on the board of 5BARz International Inc., a San Diego- based global technology leader in the cellular network extender industry and broadband networks, with India as its maiden market. He serves as the Managing Director and CEO of its wholly owned Indian subsidiary where he has been instrumental in bringing the technological innovations to market as 5BARz India Private Limited – a flagship technology start-up working to solve the last mile connectivity challenges of emerging markets.<br>

Prior to this, Samartha was CEO of MindTree Wireless Private Limited which had a 1000+ employee base (a fully owned subsidiary of MindTree Limited). Here he was responsible for setting up Smartphone and 4G base station product businesses as a non-linear revenue stream.<br>
Samartha was the founder and Managing Director of Kyocera Wireless India (KWI), Bangalore - a subsidiary of Kyocera Group of Companies headquartered in Japan. Since its inception in 2003, he established KWI as a profit center, grew it from 0 to 850+ engineers, and drove sales of over USD 200 million through phone sales in India. In his leadership, the company delivered 250 models of phones and 7 types of base stations for the global market.<br>

A passionate technocrat, he has enabled 100+ patents from the companies he led. He lives with the guiding mantra "more technologies and more products for the globe from India". His company FiDZeal, formerly known as Aseema, works on technologies to enhance enterprise productivity through a fleet of mobile applications and back-end analytics products, while Huddle N Deal provides home entertainment and automation solutions for Indian HNI homes.<br>
A Bachelor of Engineering in Electronics and Communications from Mysore University, Samartha has done Advanced Management Program from ISB, Hyderabad, and Kellogg School of Management, Chicago. He also has a Master's in Philosophy and Religion from MKU, Madurai. He is pursuing his Ph.D. in Philosophy from HUA, Orlando.<br>

Samartha is a sought-after speaker at several prestigious engineering institutions. He is also the Vice Chairman of the World Organization for Students and Youth, India. He was a special invitee to the “Karnataka Vision Group” organized by the Karnataka Government. He also served as an executive member of the Indo-Japanese Chamber of Commerce and Industries.





