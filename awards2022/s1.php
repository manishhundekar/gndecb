                                                   style="text-align: justify;"><b>General Joginder Jaswant Singh</b>, PVSM, AVSM, VSM, ADC was born on 17 September 1945. He is an Indian politician and former Army Chief. He was the 21st Chief of the Army Staff (COAS) of the Indian Army. He was appointed on 27 November 2004 and took over the role when his predecessor, General N C Vij, retired on 31 January 2005. He was succeeded by General Deepak Kapoor. He served as the COAS from 31 January 2005, to 30 September 2007.<br>

He is the first Sikh to have led the Indian Army and the 11th chief of army staff from the Western Command based at Chandimandir. His selection was not a surprise, as at the time of his appointment he was the most senior officer in the army after General N C Vij. Following his retirement, he became Governor of the state of Arunachal Pradesh on 27 January 2008.<br>

General Joginder Jaswant Singh commanded the 9th Infantry Division from 1996 to 1998. He was then selected for a key appointment as the Additional Director General Military Operations (ADGMO) at Army HQ, Military Operations Directorate. During his tenure as ADGMO, he contributed positively to evolving India's policy on the Sino-Indian border issue and visited Beijing as part of the Joint Working Group. He was also part of the Ministry of Defence team for talks with Pakistan on the Siachen and Sir Creek issue in 1998.<br>

General Singh took over as the General Officer Commanding-in-Chief of Western Command on 1 February 2004, and his tenure at this elite command provided an opportunity to put into practice the concepts and doctrines that evolved at ARTRAC. He was also appointed as Honorary aide- de-camp (ADC) to the President of India on 1 February 2004. Gen. Joginder Jaswant Singh assumed command of the Indian Army, as the 22nd chief of army staff, on 31 January 2005 commanding an army of over a million soldiers. He is a National Leader with over 48 years of contribution to nation-building and has the distinction of being the Chief of Army Staff and later Governor of Arunachal Pradesh.<br>

