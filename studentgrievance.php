                           <h5 style="background: #2F92D8;color:#fff;margin-top: 0px;margin-bottom: 10px;padding: 7px;"><i class="fas fa-address-book"></i>&nbsp Grievance Redressal Process</h5>
                           <b>1.</b> Any Person who wants to raise the grievance/ complaint can submit the same through grievance redressal portal link.<br>
                           <b>2.</b>The raised grievances will be directed to the Grievance Cell Convener.<br>
                           <b>3.</b>. If the raised matter is not a grievance, the request will be closed.<br>
                           <b>4.</b>. The grievance will be forwarded to chairman for further action for redressal.<br>
                           <b>5.</b> Once the grievance is addressed, grievant will be informed.<br>
                           <b>6.</b>. Grievances can also be raised through offline by dropping written grievances in grievance box<br>
                           <b>Publicity and Awareness</b>
                           A wide publicity and awareness is given to all the students through addressing them and through institute website. Also contact numbers of convener Grievance cell is displayed at important places in campus. Grievance boxes are also provided at various places in campus.
