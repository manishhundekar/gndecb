         <p><span style="color: #993366;"><strong>INNOVATIVE TEACHING</strong></span></p>
<p>The department consists of dedicated, qualified and experienced faculty and supporting staff in various fields of Mechanical Engineering. All the staff members meet the requirements as prescribed by AICTE and VTU University. Various pedagogical activities have been followed by the faculty for the student in order to create interest over the subjects and the developing technologies.</p>
<p>The following are the various teaching methodologies used in different aspects for the delivery of course:</p>
<p><strong>i) Lecture Method and Interactive Learning:</strong></p>
<p>1.Audiovisual aids, chalk, and board are used by the faculty for teaching.</p>
<p>2.Students are encouraged to clarify their doubts, during the lecture hour itself.</p>
<p>3.During the lecture hour first 5 minutes are used for revising topics taught in the previous class and the last 5 minutes is used to give a summary of the current class. Hence the students will get continuity of the previous day topics.</p>
<p><strong>ii) Application Based Learning:</strong></p>
<p>1.Students are encouraged to attend workshops from 2<sup>nd</sup> year onwards. As an outcome of the workshop, interested students will develop Projects/ mini Projects at the end of the workshop.</p>
<p>2.All third-year students are encouraged to do mini projects and are asked to submit the same before the commencement of exams.</p>
<p>3.Students are encouraged for an internship in the industry during vacations.</p>
<p>4.Students are taken to industrial visit at least once a semester to see the real-time operation.</p>
<p><strong>iii) Practical Learning:</strong></p>
<p>1.Practical exercises are taught in the laboratory.</p>
<p>2.Virtual Lab exercises are given to the students.</p>
<p>3.In addition to the experiments given in the syllabus, students are instructed to practice additional experiments as suggested by the concerned faculty.</p>
<p>4.Mechanical software like Autocad-2D&amp; 3D, Solid Edge, Solid Works, CATIA, Master CAM, Ansys-12 (student version) and the laboratory work is broadly based to develop an understanding of the properties of materials and design the machines and equipments</p>
<p>5.Laboratories are accessible for the students during the scheduled periods and also during leisure hours for doing experiments.</p>
<p>6.Charts are displayed in laboratories through which the students can gain basic ideas about different equipment such as Universal Testing machine, Lathe machine, Drilling machine and various Software packages.</p>
<p>7.Students are taken to the nearby manufacturing industtries once in their curriculum.</p>
<p><strong>iv) Hi-tech classroom:</strong></p>
<p>1.All classrooms are equipped with projector facility.</p>
<p>2.All laboratories and classrooms have Wi-fi access.</p>
<p><strong>v) Other Innovative Teaching methods adopted by the faculty</strong></p>
<p><em>1.Real-Time Demonstration</em> &ndash; Models are displayed in laboratories through which the students can see the models</p>
<p><em>2.KSCST Projects </em>&ndash; Students are encouraged and guided by faculty members to do real-time projects and funded by KSCST.</p>
<p><em>3.Online courses</em> &ndash; Online courses are being conducted through NPTEL online courses so that students can post and clear their doubts at any time without hesitation and can download course materials.</p>
<p><em>4.College Website</em> &ndash; Each student can download Course Material, Syllabus, Question bank, Assignment topics, Short videos, NPTEL video links.</p>
