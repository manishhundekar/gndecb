           <p><span style="color: #993366;"><strong>INNOVATIVE TEACHING</strong></span></p>
<p>The department consists of dedicated, qualified and experienced faculty and supporting staff in various fields of Electrical and Electronics Engineering. All the staff members meet the requirements as prescribed by AICTE and VTU University. Various pedagogical activities have been followed by the faculty for the student in order to create interest over the subjects and the developing technologies.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Faculty innovation in teaching is being supported and followed at the institutional level, to improve teaching and learning process.&nbsp; Innovations by the Faculty in teaching and learning shall be summarized as per the following description.</p>
<ol>
<p><span style="color: #333300;"><strong>1.Flipped classroom</strong>:</span> A flipped classroom is a&nbsp;<strong>type of blended learning</strong><strong>&nbsp;</strong>where students are encouraged to prepare for lessons prior to class, at home.&nbsp; This may involve reading course material or relevant assignments. Class time is used to discuss the lesson, share ideas and allow students to interact on the &nbsp;&nbsp;work done at home.&nbsp;</p>
</ol>
<ol start="2">
<p><strong>2.Simulated Based Education (SBE): </strong>Simulation-based education refers to the use of simulation software and tools in teaching and learning <strong>&nbsp;</strong> Topics are simulated using software tools by which the students can directly relate to the topics being taught&nbsp;</p>
</ol>
<ol start="3">
<p><strong> 3.Collaborative learning: </strong>Collaborative learning is the educational approach of using groups to enhance learning through working together.&nbsp; &nbsp; &nbsp;&nbsp;</p>
</ol>
<ol start="4">
<p><strong>4.Chart Preparation:</strong> Students are given a topic and asked to show their creativity in projecting the theme in the charts. Charts on thermal power plant and nuclear power plant are created by the teachers.<strong>&nbsp;</strong></p>
</ol>
<ol start="5">
<p><strong>5.Animated Method of Learning</strong>: Concepts which are hard to visuapze are taught using animated videos. Animated videos on single phase and three phase electrical systems as well as working of electrical motors are shown to the students.<strong>&nbsp;</strong></p>
</ol>
<ol start="6">
<p><strong>6.Debate</strong><strong>- </strong>It&rsquo;s a formal discussion on a particular topic in a classroom. Students are divided into various groups and asked them to express their views.&nbsp; Debate helps students to improve thinking, pstening and speaking skills. It also promotes students&rsquo; confidence level.&nbsp;</p>
</ol>
<ol start="7">
<p><strong>7.Practical Based learning:</strong> In practical based learning students observes real objects or materials or witnesses a demonstration.Students are taken to substations and generating stations for pve demonstration of electrical power generation and distribution.</p>
</ol>
<ol start="8">
<p><strong>8.Teaching through </strong><strong>Information and communications technology</strong>(<strong>ICT</strong>) <strong>Tools:-</strong> Educationthrough&nbsp; information and communications technology is to support, enhance, and optimize the depvery of information.&nbsp;&nbsp;ICT&nbsp;can lead to an improvement in student learning and better&nbsp;teaching&nbsp;methods.</p>
</ol>
<ol>
<p><strong> 9.Cut sections of Electrical Machines :- </strong>Cut sections of electrical machines are shown to the student for better understanding of construction and working of various machines.</p></ol>
<ol><p><strong>10.Teaching</strong> <strong>through virtual labs</strong>: A virtual lab provides remote-access to labs. It is a complete learning management system where the students can conduct experiments and avail the various tools for learning, including additional web-resources, video-lectures, animated demonstrations and self evaluation.</p></ol>
<ol><p><strong>11.Industrial exposure</strong>: Industrial exposure is a strategy where engineering students are put through real &ndash;pfe work situations. It helps to bridge the gap between the academic and the actual conditions in the industry. Students are taken to industrial visits every year.</p></ol>
</ol>
