                                                   style="text-align: justify;"><b>Shri Jitender Singh Shunty</b> was born on 1 August 1961in Delhi. He is the youngest child of the Late S. Pritpal Singh Sabharwal and the Late Mrs. Jagbir Kaur. Mr. Jitender Singh Shunty is a former BJP-Akali Dal MLA from the Shahdara constituency in the National Capital Territory of Delhi. He is a senior leader of the BJP-Shiromani Akali Dal. Jitender Singh Shunty has voluntarily donated blood 100 times and counting. He is also known as <b>Donor Singh</b>. He is the first Sikh in the world to achieve this.
<b>Shri Jitender Singh Shunty</b> is the founder of Shaheed Bhagat Singh Sewa Dal and Shaheed-Ae-Azam Bhagat Singh Foundation. The NGO was established with the objective of saving lives by promoting the availability, accessibility, and effectiveness of emergency services in the developing world. With its head office in New Delhi, India, Shaheed Bhagat Singh Sewa Dal works in more than 5 states of India. Sewa Dals approach is guided by its vision of a world where everyone avails basic emergency services and no one is denied their last rites, in which services are provided for the benefit of all.<br>

