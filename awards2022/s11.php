                                                   style="text-align: justify;"><b>Smt. Deepa Malik</b> was born on 30 September 1970 and is an Indian athlete. She started her career at the age of 30. She is the first Indian woman to win a medal in Paralympic Games and won a silver medal at the 2016 Summer Paralympics in shot put. She also won gold in the F-53/54 Javelin event at the para athletic Grand Prix held in Dubai in 2018. She is currently the world's number one in the F-53 category. She has won accolades for her participation in various adventure sports. She is associated with the Himalayan Motorsports Association (H.M.A.) and the Federation of Motor Sports Clubs of India (F.M.S.C.I.). She has undertaken an 8-day, 1,700-km drive in sub-zero temperatures which included a climb to 18,000 feet (5,500 m). It was – Raid De Himalaya. This journey covers many difficult paths including the remote Himalayas, Leh, Shimla, and Jammu.<br>

