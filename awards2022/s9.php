                                                   style="text-align: justify;"><b>Mata Jonathan</b> was born on 20 May 1957 as Manjunatha Shetty in Kallukamba village in Karnataka Bellary district to Hanumanthaiah and Jayalakshmi. She was married to Jogappa, a Hindu deity owing to a ritual and was not allowed to return home. She was then known as Manjamma Jogathi. She is an Indian theatre actress, singer, and dancer of Jogathi Nritya, a folk dance form of North Karnataka. In 2019, she became the first transgender woman to be the president of Karnataka Janapada Academy, the state's top institution for folk arts. In January 2021, the Government of India announced the Padma Shri award for her contribution to the field of folk arts.<br>

                                                  Manjamma became a permanent dancer in Kallavva Jogathi's dance group, performing in different parts of India. After Kallavva's death, Manjamma took over the troupe and made the dance popular among the people. Manjamma Jogati, the renowned practitioner of Jogati Nritya, who has been appointed as the chairperson of Karnataka Janapada Academy, is the first transwoman to head a cultural academy in Karnataka. Ms. Manjamma had earlier served as a member of the academy.<br>

                                                  In 2006, she received the Karnataka Janapada Academy Award. In 2010, the Karnataka government honored her with the annual Kannada Rajyotsava award. Transgender folk dancer Manjamma Jogati received a Padma Shri award the fourth highest civilian award of the Republic of India in 2021 for her contribution to the arts. Manjamma mastered, among other art forms, Jogati Nritya and Janapada songs, and Kannada language sonnets in praise of various female deities.


