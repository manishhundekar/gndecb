                <b> (As per the VTU circular VTU/BGM/ACA/SA/Circlr/2023-24/822/ and UGC notification dated 11.04.2023)</b><br>
The Student Grievance Redressal Committee is intended to undertake the processes of attending to the grievances put forward by the students. It focuses on setting proper facilitation procedures for settling various issues in a cordial atmosphere. The committee is expected to initiate proper or appropriate enquiry or investigative mechanism at the earliest from the receipt of the complaint in written form. The committee is expected to meticulously adhere to the standard arbitration procedures of the institute and those of relevant law prepositions including Industrial Disputes Act 1947, the Administrative Tribunal Act 1985, Negotiable Instruments Act 1881, Societies Registration Act 1860 and all other such enactments of the Government of Karnataka and Government of India from time to time.<br>
<b>Duties and responsibilities of this committee</b><br>
1. To receive the grievances from students in writing which can be through online and off line<br>
2. To direct the grievances to appropriate committee for redressal with request to resolve the same within time frame which committee feels appropriate.<br>
3. Give feedback to the aggrieved regarding status of his grievance<br>

<b>Frequency of meeting: Monthly</b>

