             <p> The department of Electronics and Communication Engineering has been established in the year 1982 with an intake capacity of 30 seats which  progressively increased  to 120 seats over a period of time. Apart from the UG course,the department offers Postgraduate Course in Digital Communication and Networking. The department also  has a Research Center with state of the art infrastructure and facilities for Research activities on Core and Interdisciplinary domains . The departmenpt boasts of a  team of highly  qualified, dedicated and experienced faculty. In the department, 33% of faculty are doctorates and 33%  are pursuing Ph.D from reputed Institutes across the country. E&CE offers a great ambience for the wholesome development of students.</p>
<p>With an ever growing call quality education and research, the department is committed to excellence in education. It has stringent recruitment policies, high standards for academic performance, outstanding infrastructure and proactive placement cell. The students are rigorously trained in their academic curriculum and also in skills that are required for the industry. They are given customized and employability training both in technical as well as in communication skills.Department runs VLSI training program with SEER Academy, Hyderabad.</p>
