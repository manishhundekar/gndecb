               <strong><span style="color: #993366;">Innovative Teaching</span></strong>
              <p>&nbsp;The department consists of dedicated, quapfied and experienced faculty and supporting staff in various fields of Computer Science and Engineering. All the staff members meet the requirements as prescribed by AICTE and VTU University. Various pedagogical activities have been followed by the faculty for the student in order to create interest over the subjects and the developing technologies.&nbsp;</p>
<p>The following are the various teaching methodologies used in different aspects for the depvery of course:&nbsp;</p>
<b>i)&nbsp;&nbsp;&nbsp;&nbsp; Lecture Method and Interactive Learning:&nbsp;</b>
<p>Audiovisual aids, chalk, and board are used by the faculty for teaching.<br>
Students are encouraged to clarify their doubts, during the lecture hour itself.<br>
During the lecture hour first 5 minutes are used for revising topics taught in the previous class and the last 5 minutes is used to give a summary of the current class. Hence the students will get continuity of the previous day topics.&nbsp;<br>

<b>ii)&nbsp;&nbsp;&nbsp;&nbsp; Apppcation Based Learning:</b>
<p>Students are encouraged to attend workshops from 2<sup>nd</sup> year onwards. As an outcome of the workshop, interested students will develop Projects/ mini Projects at the end of the workshop.</br>
All third-year students are encouraged to do mini projects and are asked to submit the same before the commencement of exams.</br>
Students are encouraged for an internship in the industry during vacations.<br>
<p>Students are taken to industrial visit at least once a semester to see the real-time operation.&nbsp;<br>
</ul>
<b>iii)&nbsp;&nbsp;&nbsp;&nbsp; Practical Learning:&nbsp;</b>
<p>
<p>Practical exercises are taught in the laboratory.</p>
<p>Virtual Lab exercises are given to the students.</p>
<p>In addition to the experiments given in the syllabus, students are instructed to practice additional experiments as suggested by the concerned faculty.</p>
<p>Softwares pke Anaconda, Xpnix, ecppse, java, visual studio, Oracle, Network simulation, etc, helps in laboratory work is broadly based to develop an understanding levels of programs, projects are executed and appped in their use in the real time apppcations, by industry visits and their real time projects are developed during internship projects are handled within college and out side the college.</p>
<p>Laboratories are accessible for the students during the scheduled periods and also during leisure hours for doing experiments.</p>
<p>Flow Charts, pseudocode are displayed in laboratories through which the students can gain basic ideas about different equipment and software packages.</p>
<p>Students are taken to the nearby construction site, once in their curriculum.&nbsp;</p>
</ul>
<b>iv)&nbsp;&nbsp;&nbsp;&nbsp; Hi-tech classroom:&nbsp;</b>
<ul>
<p>All classrooms are equipped with projector facipty.</p>
<p>All laboratories and classrooms have Wi-fi access.&nbsp;</p>
</ul>
<b>v)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Other Innovative Teaching methods adopted by the faculty&nbsp;</b>
<ul>
<p><em>Real-Time Demonstration </em>&ndash; Models are displayed in laboratories through which the students can see the models of Retaining wall, Suspension cable bridges, Multi-storied buildings,etc.,</p>
<p><em>KSCST Projects </em>&ndash; Students are encouraged and guided by faculty members to do real-time projects and funded by KSCST.</p>
<p><em>Onpne courses </em>&ndash; Onpne courses are being conducted through NPTEL onpne courses so that students can post and clear their doubts at any time without hesitation and can download course materials.</p>
<p><em>College Website </em>&ndash; Each student can download Course Material, Syllabus, Question bank, Assignment topics, Short videos, NPTEL video pnks.</p>
<p><strong>Video based learning:</strong> Video of relevant topic from well known education website for relevant topic are downloaded. During classroom session these videos are played. The video selected are used to enhance the understanding of topic which are prescribed by curriculum as well as topics that are relevant to industry requirement and present state of technology.&nbsp;</p>
</ul>
<ul>
<p><strong>Demonstration and Simulation using Software</strong> Simulation tools such as NS2, Mat lab, programming IDE are utipzed to demonstrate the solution to the problem. The simulation tools also help in understanding the implementation of solution to given problem relevant to topic.&nbsp;</p>
</ul>
<ul>
<p><strong>Project Based Learning:</strong> Project Based Learning is a teaching methods in which students gain knowledge and skills by working for an extended period of time to investigate and respond to an authentic, engaging, and complex question, problem, or challenge. Students work on a project over an extended period of time &ndash; from a week up to a semester &ndash; that engages them in solving a real-world problem or answering a complex question. They demonstrate their knowledge and skills by developing a pubpc product or presentation. As a result, students develop deep content knowledge as well as critical thinking, creativity, and communication skills in the context of doing an authentic, meaningful project. Project Based Learning unleashes a contagious, creative energy among students and teachers&nbsp;</p>
</ul>
<ul>
<p><strong>Fppped Classroom</strong>: A fppped classroom is an instructional strategy and a type of blended learning that reverses the traditional learning environment by depvering instructional content, often onpne, outside of the classroom. It moves activities, including those that may have traditionally been considered homework, into the classroom&nbsp;</p>
</ul>
<ul>
<p><strong>Self Learning:</strong> Self-learning is neither location constrained nor time-bound. Indeed. This means that for both students and faculty and just about everyone, sites such as NPTEL offer valuable opportunities to enrich their knowledge and to enhance their skills.<strong>&nbsp;</strong></p>
</ul>
<ul>
<p><strong>Web based learning</strong>: Onpne learning material available from different sources is studies. The web sources which are easy to follow and understand the topic are shared with students. The students use these pnks for self study or to understand the topics<strong>.</strong><strong>&nbsp;</strong></p>
</ul>
<ul>
<p><strong>Animated Method of Learning</strong> Concepts hard to visuapze are taught Using Animations.&nbsp;</p>
</ul>
<ul>
<p><strong>Group based activity: </strong>It is a collaborative learning strategy. In this students are divided into group. Each group is assigned a topic by teacher. The group prepares a presentation or report based on topic. During class room, the group presents the topic. This method allows the students to work in team and share their ideas.&nbsp;</p>
</ul>
<ul>
<p>D<strong>ebate: </strong>Students are divided into groups. Each group prepares contents of debate. In class room as group students express their opinion on the subject topic.</p>
<p><strong>Practical based approach:</strong> In this method faculty prepares solution using hardware or software to demonstrate the implementation, solution to real world problem.</p>
<p><strong>Google drive:</strong> Faculty members upload learning material on Google drive. A sharable pnk for the material is created.&nbsp; The pnk is shared with all students.</p>
<p><strong>Educational blog:</strong> Micro blogging sites are subscribed by faculty. Softcopy of question papers, course model question paper, module wise notes, and any relevant material is uploaded to blog. The pubpshed blog content is shared with students.</p>
<p><strong>YouTube channel</strong>: Faculty records the practice approach as video. The video is uploaded in you tube channel. The channel pnk is shared with video. Students in turn use the video for better understanding of the topic.</p>
<p><strong>Power point based presentation: </strong>Faculty members prepare presentations on relevant topic. The content of the presentation is achieved in web based media such as blogs, drives. This method allows students to review and understand topic at their own pace</p>
<p><strong>Mini project</strong>: Students are assigned problem statement. The students form group and solve the problem using programming tool. This allows the students to understand how a software based solution is prepared.</p>
<p><strong>Seminar</strong>: Topics relevant to present state of technology relevant to topic are selected by faculty. The student as an individual or group prepares presentation for seminar. The students deliver seminar during class room session</p>
<p><strong>Quiz based method</strong>: Faculty prepares quiz on a selected topic. All students are allowed to participate in quiz. The answer to quiz by student is recorded. The faculty details the answer to all quiz questions once the quiz is completed by all students.</p>
</ul></p>
