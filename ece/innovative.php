         <h4><span style="color: #993366;"><strong>Content Delivery Strategies:</strong></span></h4>
<p>Faculty members at ECE use an array of teaching strategies as there is no single, universal approach that improves the Program Learning Outcomes. Depending upon the learning requirement, combinations of strategies are used for content delivery. The Course Coordinator and faculty members pre-decide on teaching methods to be adopted and the same is recorded in the course file.</p>
<p>&nbsp;Different content delivery strategies adopted by faculty members of ECE department for course delivery are as follows:</p>
<p><strong>Course File: </strong>Faculty members at GNDEC maintain Course File for each course in a standard format detailing the course information including syllabus, course objectives, its policy, lesson plan with proposed mode of delivery, List of standard and reference books, journals, websites, online materials, mapping of COs and POs, question bank relating to the course. A course file is prepared by the Course Coordinator in consultation with all faculty members taking the course. The course file facilitates improvement of quality and mode of delivery and its monitoring.</p>
<p><strong>Lesson Plans: </strong>At the beginning of the semester the lesson plans are uploaded on to the department website and students are asked to make use of that so that they can know the extent of teaching in each class. Faculty members follow the lesson plans and in case of gaps, special classes, guest lecture etc are taken.</p>
<p><strong>Lectures: </strong>Class room lecturers are beneficial in deliberating theoretical aspects with more logical and numerical problems. In every lecture hour students are refreshed by pre and post summary. Lectures are a quick and efficient way of introducing large numbers of students. Lectures are usually interactive in nature to promote active learning.</p>
<p><strong>Tutorials: </strong>Tutorials are beneficial to deliberate logical and numerical workouts. In the tutorial hour students&rsquo; hypothetical knowledge is updated with analytical workouts.</p>
<p><strong>Presentations using Audio Visual Aids: </strong>Presentations are given to illustrate intricate ideas and concepts in graphic form. Presentations give information with data relating to an issue. Videos effectively communicate the working of actual Engineering solutions and their impact. Presentations are always followed by discussions.</p>
<p><strong>Online Classes: </strong>During Covid19 pandemic situation, all faculty members conducted online classes for content delivery and this has been very well accepted by students. Faculty members plan to continue with online classes in the post Covid period to cover some of the topics during holidays or post college hours as an additional delivery strategy in addition to regular classes.</p>
<p><strong>Assignments: </strong>Students are given the course assignment from course syllabus. These assignments help students understand and practice promoting self-learning and improving critical thinking ability. Assignments also help students to gain confidence on course for SEE.</p>
<p><strong>Self-Study: </strong>Self-study assignments are given to the student and they are required to prepare and present the same.</p>
<p><strong>Handouts: </strong>Handouts are given to supplement lectures. Handouts contain information for wider understanding of the philosophy of a subject and how it relates to the contemporary world.</p>
<p><strong>E-Resources: </strong>E- Resources are posted on the student resource. E- Resources pertain to supplementary knowledge required by the students by self-study.</p>
<p><strong>Flip classes/Roll over Method: </strong>Flip classes are conducted in the department to enhance student self- learning ability, where students are allowed to use online lectures and participate in online discussions. The same is engaged and discussed in the regular lecture hours.</p>
<p><strong>Laboratory based learning: </strong>Apply concepts learned in class to practical situations.</p>
<p><strong>Collaborative Learning: </strong>It is the educational approach of using groups to enhance learning through working together. Groups of two or more learners work together to solve problems, complete tasks or learn new concepts.</p>
<p><strong>Think and Speak: </strong>In this approach Students are given with few topics in the course and asked to think and speak on that.</p>
<p><strong>Google It: </strong>In this teaching method, students are assigned to do the literature review of a few important topics and asked to note down the things. At the end of the class, collective information of the topic/s is discussed by the students.</p>
<p><strong>Teaching through research papers: </strong>In this activity, students have been assigned to survey research papers on the specified topic and present the same after having group discussion on that topic in the class.</p>
<p><strong>Z-A approach: </strong>In this teaching method, applications, advantages, and features of a few important topics are brainstormed in the reverse order of the syllabus covered.</p>
<p><strong>Simulated Software Based Learning: </strong>Simulation is done to enhance practical knowledge of the students. Circuits/models are simulated using software tools by which the students can functionally relate to the topics being taught.</p>
<p><strong>Quiz: </strong>Teachers conduct quiz sessions to help students understand concepts as well as to have a first-hand assessment on students learning.</p>
<p><strong>Learner Centric Mode of Education: </strong>Various Learner-Centric modes of teaching-learning such as MOOCS, Swayam-NPTEL materials are used that makes learning exciting and self-driven by the students.</p>
<p>&nbsp;</p>
