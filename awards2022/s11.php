                                                  <b>Achievements</b><br>

                                                 <b> Smt. Deepa Malik </b>is the first Indian woman to win a medal at the Paralympics. She won the silver medal in the shot put in 2016 Paralympic Games. She was previously honored with the Arjuna award in 2012, at the age of 42 years. She has also been conferred the prestigious Padma Shri award in 2017. She created a New Asian Record in Asian Para Games 2018 and is the only Indian woman to win medals in 3 consecutive Asian Para Games (2010, 2014, 2018). She has won 58 national and 23 international medals across all disciplines to date.



